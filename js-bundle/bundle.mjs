/**
 * esbuild bundler — converts ESM Mermaid into a single IIFE bundle
 * that GraalJS (Java 8) can load via eval().
 *
 * Usage:  npm install && npm run bundle
 * Output: ../src/main/resources/mermaid/mermaid.min.js
 */
import * as esbuild from 'esbuild';

const outfile = '../src/main/resources/mermaid/mermaid.min.js';

await esbuild.build({
    entryPoints: ['entry.mjs'],
    bundle: true,
    format: 'iife',
    globalName: '__mermaid_bundle',
    outfile,
    minify: true,
    target: ['es2020'],
    // Mermaid uses 'require' in some optional code paths — mark as external
    // to prevent build failures (not needed at runtime in our shim env)
    external: ['require'],
    banner: {
        js: [
            '// ═══════════════════════════════════════════════════════════════',
            '// Mermaid IIFE bundle — AUTO-GENERATED by esbuild, DO NOT EDIT',
            '// Source: npm mermaid (see js-bundle/package.json for version)',
            '// Rebuild: cd js-bundle && npm install && npm run bundle',
            '// ═══════════════════════════════════════════════════════════════',
        ].join('\n'),
    },
    footer: {
        js: [
            '',
            '// Expose mermaid on the global window object for GraalJS',
            'if (typeof window !== "undefined") {',
            '  var __m = __mermaid_bundle && (__mermaid_bundle.mermaid || __mermaid_bundle.default || __mermaid_bundle);',
            '  if (__m) window.mermaid = __m;',
            '}',
        ].join('\n'),
    },
});

console.log('✓ Bundle written to ' + outfile);

